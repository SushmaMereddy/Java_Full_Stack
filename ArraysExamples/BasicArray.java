class BasicArray{
	public static void main(String[] args){
		int[] marks=new int[3];
		marks[0]=100;
		marks[1]=95;
		marks[2]=80;
		
		System.out.println("Marks lenght :: "+marks.length);
		System.out.println("Value of Index 0 :: " +marks[0]);
		System.out.println("Value of Index 1 :: "+marks[1]);
		System.out.println("Value of Index 2 :: "+marks[2]);
		System.out.println(" ");
		
		int temp[]={200,300,400,500};
		
		System.out.println("Temp length :: "+temp.length);
		System.out.println("Value at Index0 for Temp ::"+temp[0]);
		System.out.println("Value at Index1 for Temp ::"+temp[1]);
		System.out.println("Value at Index2 for Temp ::"+temp[2]);
		System.out.println("Value at Index3 for Temp ::"+temp[3]);
		
	}
}
