class SwitchDemoOpt{
	
	public static void main(String[] args){
		char opt='S';
		switch(opt){
			case 'I': System.out.println("IPHONE");
						break;
			case 'S': System.out.println("SAMSUNG");
						break;
			case 'N': System.out.println("NOKIA");
						break;
			case 'B': System.out.println("BLACKBERRY");
						break;
			default : System.out.println("NO MOBILE OPTION");
						
		}
		
	}
}